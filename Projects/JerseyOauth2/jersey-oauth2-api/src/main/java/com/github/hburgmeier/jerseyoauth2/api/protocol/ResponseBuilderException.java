package com.github.hburgmeier.jerseyoauth2.api.protocol;

public class ResponseBuilderException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ResponseBuilderException() {
		super();
	}

	public ResponseBuilderException(String message, Throwable cause) {
		super(message, cause);
	}

	public ResponseBuilderException(String message) {
		super(message);
	}

	public ResponseBuilderException(Throwable cause) {
		super(cause);
	}

}
